#include<iostream>

int main5() {

	// 比较运算符
	int a = 10;
	int b = 20;

	// ==
	std::cout << (a == b) << std::endl; // 0 

	// !=
	std::cout << (a != b) << std::endl; // 1

	// >
	std::cout << (a > b) << std::endl; // 0

	// <
	std::cout << (a < b) << std::endl; // 1

	// >=
	std::cout << (a >= b) << std::endl; // 0

	// <=
	std::cout << (a <= b) << std::endl; // 1

	return 0;
}