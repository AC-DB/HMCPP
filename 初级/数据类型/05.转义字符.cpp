#include<iostream>

int main5() {

	// 转义字符

	// 换行
	std::cout << "\n" << std::endl;

	// 打印反斜杠
	std::cout << "\\" << std::endl;

	// 水平制表符 （8个空格）
	std::cout << "aa\tHello" << std::endl;
	std::cout << "aaaa\tHello" << std::endl;
	std::cout << "a\tHello" << std::endl;

	// 换行
	std::cout << "\n" << std::endl;

	return 0;
}