#include<iostream>

int main1() {

	// 整型
	// 短整型(-32768~32767)
	short num1 = 32767;
	// 整型
	int num2 = 10;
	// 长整型
	long num3 = 10;
	// 长长整型
	long long num4 = 10;

	std::cout << "num1=" << num1 << std::endl;
	std::cout << "num2=" << num2 << std::endl;
	std::cout << "num3=" << num3 << std::endl;
	std::cout << "num4=" << num4 << std::endl;

	return 0;
}